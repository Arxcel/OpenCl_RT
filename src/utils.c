#include "ft_rt.h"

t_vector	v_cross(t_vector a, t_vector b)
{
	t_vector c;

	c[0] = a[1] * b[2] - a[2] * b[1];
	c[1] = a[2] * b[0] - a[0] * b[2];
	c[2] = a[0] * b[1] - a[1] * b[0];
	return (c);
}
