#include "ft_libftu.h"

static void	ft_recur(unsigned int n, int fd)
{
	if (n / 10)
		ft_recur(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}

void		ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_recur(-n, fd);
	}
	else
		ft_recur(n, fd);
}
