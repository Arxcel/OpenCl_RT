#include "ft_libftu.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *tmp;

	tmp = (unsigned char *)s;
	while (n > 0)
	{
		if (*(unsigned char *)tmp == (unsigned char)c)
			return ((void *)tmp);
		tmp++;
		n--;
	}
	return (0);
}
