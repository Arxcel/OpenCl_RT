#include "ft_libftu.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	if (s1 && s2)
	{
		while (n > 0 && s1[i] && s2[i])
		{
			if (s1[i] == s2[i] && n--)
				i++;
			else
				return (0);
		}
		return (1);
	}
	return (0);
}
