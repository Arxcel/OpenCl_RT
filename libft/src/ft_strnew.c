#include "ft_libftu.h"

char	*ft_strnew(size_t size)
{
	char *tmp;

	if ((tmp = (char *)malloc(size + 1)))
	{
		tmp = (char *)ft_memset(tmp, '\0', size + 1);
		return (tmp);
	}
	return (NULL);
}
