int		ft_sqrt(int nb)
{
	int i;
	int sq;

	i = 1;
	sq = 0;
	while (i <= nb / 2 && nb > 0)
	{
		sq = i * i;
		if (sq == nb)
			return (i);
		i = i + 1;
	}
	return (0);
}
