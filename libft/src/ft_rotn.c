char	*ft_rotn(char *str, int n)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] = 'A' + (str[i] - 'A' + n) % 26;
			else if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = 'a' + (str[i] - 'a' + n) % 26;
			i++;
		}
		return (str);
	}
	return (0);
}
